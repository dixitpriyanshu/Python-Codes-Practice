def greet():
    print("Hello")
    print("How are you?")

greet()

def greet_with_name(name):
    print(f"Hello {name}")
    print(f"How are you {name}")

greet_with_name("Angela")

def greet_with(name, location):
    print(f"Hello {name}")
    print(f"How is it like in {location}")

greet_with("Angela","LONDON")